package Heap;

import java.util.Scanner;

public class HeapLogic {

	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		String fruits= sc.next();
		
		switch(fruits) {
		
		case "4" -> System.out.println("I am a Little Star");
		case "Acha" -> System.out.println("Acha is Jayakumar");
		
			default ->System.out.println("Enter the order");
		}
		
	}}
